public class areaandperimeterofrect {
    public static void main(String[] args) {
        int l = 5;
        int b = 6;

        int area = l*b;
        int perimeter = 2*(l+b);

        System.out.println("Area of rectangle is " + area);
        System.out.println("Perimeter of rectangle is " + perimeter);
    }
}
